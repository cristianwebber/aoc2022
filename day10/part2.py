def compute(s: str) -> list[str]:
    X = 1
    cycle_X = []
    draw = []

    lines = s.splitlines()
    for line in lines:
        if line == 'noop':
            cycle_X.append(X)
        else:
            cycle_X.append(X)
            cycle_X.append(X)
            X += int(line.split(' ')[1])

    for i in range(240):
        mid = cycle_X[i]
        if i % 40 in (mid-1, mid, mid+1):
            draw.append('#')
        else:
            draw.append('.')

    final = [''.join(draw[40*i:40*(i+1)]) for i in range(6)]

    for i in final:  # type: ignore
        print(i)

    print('\n\n')

    return final


